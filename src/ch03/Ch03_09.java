package ch03;

public class Ch03_09 {
    public static void main(String[] args){
        long x;

        x = 123456789L;         //設定10進位長整數
        System.out.println("列印123456789L的值 = " + x);
        x = 1_000_200;          //設定含底線整數
        System.out.println("列印1_000_200 的值 = " + x);
        x = 2_000;              //設定含底線整數
        System.out.println("列印2_000     的值 = " + x);

    }
}
